void defaultdrawText(int x, int y, const char *str, uint16_t *buffer)
{
   if (y > LCD_HEIGHT || y + defaultFont.height < 0)
      return;

   int len = strlen(str);
   Tile *letters;

   while (len * defaultFont.width + x > LCD_WIDTH)
      len--;

   if (len > 0)
      letters = malloc(len * sizeof(Tile));
   else
      return;

   for (int i = 0; i < len; i++) {
      letters[i].x = x + defaultFont.width * i;
      letters[i].y = y + defaultFont.height * i;
      letters[i].index = str[i] < 32 || str[i] > 126 ? 0 : str[i] - 32;
      letters[i].parent = &defaultFont;
   }

   drawTiles(letters, len, buffer);

   free(letters);
}


void drawText(Tileset font, int x, int y, const char *str, uint16_t *buffer)
{
   if (y > LCD_HEIGHT || y + font.height < 0)
      return;

   int len = strlen(str);
   Tile *letters;

   while (len * font.width + x > LCD_WIDTH)
      len--;

   if (len > 0)
      letters = malloc(len * sizeof(Tile));
   else
      return;

   for (int i = 0; i < len; i++) {
      letters[i].x = x + font.width * i;
      letters[i].y = y + font.height * i;
      letters[i].index = str[i] < 32 || str[i] > 126 ? 0 : str[i] - 32;
      letters[i].parent = &font;
   }

   drawTiles(letters, len, buffer);

   free(letters);
}
